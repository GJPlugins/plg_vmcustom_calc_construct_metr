    JLoader::register('GtmFormField' , JPATH_PLUGINS.'/system/plg_vmcustom_calc_construct_metr/models/fields/GtmFormField.php');

    class GtmFormFieldText extends \GtmFormField
    {

        protected function getInput()
        {
            $LayoutData = $this->getLayoutData();
            $LayoutData['button'] = $this->getAttribute('button');
            $LayoutData['button_onclick'] = $this->getAttribute('button_onclick');
            $LayoutData['button_icon'] = $this->getAttribute('button_icon');

            $buttonHtml = '';
            if( $LayoutData['button'] )
            {
                $buttonHtml = $this->getRenderer('renderbutton')->render($LayoutData);
            }#END IF

            return $this->getRenderer('text')->render($LayoutData) . $buttonHtml;
        }


    }


