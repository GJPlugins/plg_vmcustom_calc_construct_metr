    class vccmViewFields_product extends \Joomla\CMS\MVC\View\HtmlView
    {
        /**
         * @var void
         * @since 3.9
         */
        protected $FieldPars;
        /**
         * @var mixed
         * @since 3.9
         */
        protected $field;


        /**
         * @throws Exception
         * @since 3.9
         */
        public function display( $tpl = null )
        {
            /**
             * @var VccmModelFields_product $model
             */
            $model = $this->getModel();
            $this->FieldPars = $model->getFieldsProduct();

            if( !empty( $this->FieldPars ) )
            {
                echo '<input type="hidden" name="customProductData[587][69]" value="7266">';
            }#END IF

            foreach ($this->FieldPars as &$fieldPar)
            {
                switch ( $fieldPar->type )
                {
                    case 'modify_price' :

                        $this->field = $fieldPar;
                        echo $this->loadTemplate('modify_price');

                        break;

                }

            }#END FOREACH



        }


    }















